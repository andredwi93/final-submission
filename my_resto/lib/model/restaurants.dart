import 'dart:convert';

ListRestaurants listRestaurantsFromJson(String str) =>
    ListRestaurants.fromJson(json.decode(str));

String listRestaurantsToJson(ListRestaurants data) =>
    json.encode(data.toJson());

class ListRestaurants {
  ListRestaurants({
    this.restaurants,
  });

  List<Restaurant> restaurants;

  factory ListRestaurants.fromJson(Map<String, dynamic> json) =>
      ListRestaurants(
        restaurants: List<Restaurant>.from(
            json["restaurants"].map((x) => Restaurant.fromJson(x))),
      );

  Map<String, dynamic> toJson() => {
        "restaurants": List<dynamic>.from(restaurants.map((x) => x.toJson())),
      };
}

class Restaurant {
  Restaurant({
    this.id,
    this.name,
    this.description,
    this.pictureId,
    this.city,
    this.rating,
  });

  String id;
  String name;
  String description;
  String pictureId;
  String city;
  double rating;

  factory Restaurant.fromJson(Map<String, dynamic> json) => Restaurant(
        id: json["id"],
        name: json["name"],
        description: json["description"],
        pictureId: json["pictureId"],
        city: json["city"],
        rating: json["rating"].toDouble(),
      );

  Map<String, dynamic> toJson() => {
        "id": id,
        "name": name,
        "description": description,
        "pictureId": pictureId,
        "city": city,
        "rating": rating,
      };
}
